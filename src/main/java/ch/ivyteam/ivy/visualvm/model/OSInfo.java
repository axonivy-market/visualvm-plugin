package ch.ivyteam.ivy.visualvm.model;

public class OSInfo {

  private String fName;
  private String fArch;

  public String getName() {
    return fName;
  }

  public void setName(String name) {
    this.fName = name;
  }

  public String getArch() {
    return fArch;
  }

  public void setArch(String arch) {
    this.fArch = arch;
  }

}
